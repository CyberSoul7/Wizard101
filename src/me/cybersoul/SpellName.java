	private static final List<SpellName> VALUES = Collections.unmodifiableList(Arrays.asList(values()));
	private static final int SIZE = VALUES.size();
	private static final Random RANDOM = new Random();
	
	public static SpellName randomSpellType(int min, int max) {
		if (max <= 0) {
			return VALUES.get(RANDOM.nextInt(SIZE));
		} else if (max < SIZE) {
			return null;
		} else {
			return VALUES.get(RANDOM.nextInt((max - min) + 1) + min);
		}
	}
	
